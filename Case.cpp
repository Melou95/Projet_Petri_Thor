#include "Case.h"
#include "Bacterie.h"
#include <map>
using namespace std;


// =============================================================================
//                              Constructors
// =============================================================================
Case::Case(){
  milieu_['A']=0.0;
  milieu_['B']=0.0;
  milieu_['C']=0.0;
  p_bact_ = nullptr;
}
// =============================================================================
//                               Destructors
// =============================================================================
Case::~Case(){
  delete p_bact_;
}
// ===========================================================================
//                                Getters
// ===========================================================================
  map<char,float> Case::milieu(){
    return milieu_;
  }
  
  Bacterie * Case::p_bact(){
    return p_bact_;
  }
// ===========================================================================
//                                Setters
// ===========================================================================
void Case::set_p_bact(Bacterie bact){
    if (p_bact_!=nullptr){
      delete p_bact_;
    }
    if (bact.type()=='S'){
      p_bact_=new S(bact);
    }
    else if (bact.type()=='L'){
      p_bact_=new L(bact);
    }
  }
  
  
void Case::set_milieu(float a, float b, float c){
    milieu_['A']=a;
    milieu_['B']=b;
    milieu_['C']=c;
  }

