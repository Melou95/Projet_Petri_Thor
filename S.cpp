// =============================================================================
//                                 Includes
// =============================================================================
#include "S.h"
#include "Bacterie.h"


// =============================================================================
//                              Constructors
// =============================================================================
S::S() : Bacterie() {
  type_='S';
  RBB_=0.1;
  RBC_=0.1;
  RAA_=0;
  RAB_=0;
}

S::S(const S& bact) : Bacterie() {
  type_='S';
  RBB_=bact.RBB_;
  RBC_=bact.RBC_;
}

S::S(Bacterie* bact) : Bacterie() {
  type_='S';
  RBB_=bact->RBB();
  RBC_=bact->RBC();
}

// =============================================================================
//                               Getters
// =============================================================================
float S::RBB(){
  return RBB_;
}

float S::RBC(){
  return RBC_;
}

float S::RAA(){
  return RAA_;
}

float S::RAB(){
  return RAB_;
}

// =============================================================================
//                               Destructors
// =============================================================================
S::~S()=default;
